const express = require("express");
const auth = require("../../middlewares/auth");
const validate = require("../../middlewares/validate");
const userValidation = require("../../validations/user.validation");
const userController = require("../../controllers/user.controller");
const { uploadFiles } = require("../../utils/multer");
const multer = require("multer");

const router = express.Router();
const upload = multer();


router.patch(
  "/profile",
  auth(),
  uploadFiles,
  validate(userValidation.updateUserProfile),
  userController.updateUserProfile
);

router
  .route("/teacher")
  .get(auth("commonPermission"), userController.getTeacher);

router
  .route("/")
  .post(
    auth("manageUsers"),
    validate(userValidation.createUser),
    userController.createUser
  )
  .get(
    auth("getUsers"),
    validate(userValidation.getUsers),
    userController.getUsers
  );

router
  .route("/:userId")
  .get(
    auth("getUsers"),
    validate(userValidation.getUser),
    userController.getUser
  )
  .patch(
    auth("manageUsers"),
    validate(userValidation.updateUser),
    userController.updateUser
  )
  .delete(
    auth("manageUsers"),
    validate(userValidation.deleteUser),
    userController.deleteUser
  );

router
  .route("/export")
  .post(
    auth("commonPermission"),
    userController.exportTeachers
  );

router
  .route("/import")
  .post(
    auth("commonPermission"),
    upload.single("file"),
    validate(userValidation.importTeachers),
    userController.importTeacherFromExcel
  );

module.exports = router;

