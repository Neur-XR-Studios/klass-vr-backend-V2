#!/bin/bash

# Update package list
echo "Updating package list..."
sudo apt-get update

# Install Node.js and npm (if not already installed)
echo "Installing Node.js and npm..."
sudo apt-get install -y nodejs npm

# Install ffmpeg and python3
echo "Installing ffmpeg and python3..."
sudo apt-get install -y ffmpeg python3 python3-pip

# Install ALL Puppeteer dependencies
echo "Installing Puppeteer dependencies..."
sudo apt-get install -y \
    ca-certificates \
    fonts-liberation \
    libasound2 \
    libatk-bridge2.0-0 \
    libatk1.0-0 \
    libc6 \
    libcairo2 \
    libcups2 \
    libdbus-1-3 \
    libexpat1 \
    libfontconfig1 \
    libgbm1 \
    libgcc1 \
    libglib2.0-0 \
    libgtk-3-0 \
    libnspr4 \
    libnss3 \
    libpango-1.0-0 \
    libpangocairo-1.0-0 \
    libstdc++6 \
    libx11-6 \
    libx11-xcb1 \
    libxcb1 \
    libxcomposite1 \
    libxcursor1 \
    libxdamage1 \
    libxext6 \
    libxfixes3 \
    libxi6 \
    libxrandr2 \
    libxrender1 \
    libxss1 \
    libxtst6 \
    lsb-release \
    wget \
    xdg-utils

echo "Installation complete! Please restart your application."
